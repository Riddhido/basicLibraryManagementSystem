    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
       String mid=txtMemberId.getText();
       String member=txtMemberName.getText();
       String book=txtBook.getText();
       String returndate=txtReturnDate.getText();
       String elpdays=txtDateElap.getText();
       String fine=txtFine.getText();
            
        try {
            pst=con.prepareStatement("insert into returnbook(id,mid,mname,bname,returndate,elp,fine)values(?,?,?,?,?,?,?)");
            pst.setString(1,mid);
            pst.setString(2,member);
            pst.setString(3,book);
            pst.setString(4,returndate);
            pst.setString(5,elpdays);
            pst.setString(6,fine);
            int k=pst.executeUpdate();
            
            pst=con.prepareStatement("delete * from issuebook where memberid=?");
            pst.setString(1,mid);
            pst.executeUpdate();
            if(k==1)
            {
                JOptionPane.showMessageDialog(this,"Book Returned Successfully");
                
                txtMemberId.setText("");
                txtMemberName.setText("");
                txtBook.setText("");
                txtReturnDate.setText("");
                txtDateElap.setText("");
                txtFine.setText("");
                
                txtMemberId.requestFocus();
               ReturnBook_Load();
            }
            else
            {
                JOptionPane.showMessageDialog(this,"Error!!");
            }
        } catch (SQLException ex) {
            Logger.getLogger(ReturnBook.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

