    private void txtMemberIdKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtMemberIdKeyPressed
        if(evt.getKeyCode()==KeyEvent.VK_ENTER)
        {
        try {
            String id=txtMemberId.getText();
        
            pst=con.prepareStatement("select m.name,b.bname,l.returndate,DATEDIFF(now(),l.returndate) as elap from issuebook l JOIN member m ON l.memberid=m.id JOIN book b ON l.bookid=b.id");
            
            //pst.setString(1,id);
            rs=pst.executeQuery();
            
            if(rs.next()==false)
            {
                JOptionPane.showMessageDialog(this,"Member ID not found!");
            }
            
            else
            {
                String member_name=rs.getString("m.name");
                String book_name=rs.getString("b.bname");
                String date=rs.getString("l.returndate");
                String elp=rs.getString("elap");
                
                txtMemberName.setText(member_name.trim());
                txtBook.setText(book_name.trim());
                txtReturnDate.setText(date);
                
                int elapsed=Integer.parseInt(elp);
                
                if(elapsed>0)
                {
                    txtDateElap.setText(elp);
                    int fine=elapsed*100;
                    txtFine.setText(String.valueOf(fine));
                }
                
                else
                {
                    txtDateElap.setText("0");
                    txtFine.setText("0");
                }
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(ReturnBook.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
    }//GEN-LAST:event_txtMemberIdKeyPressed

